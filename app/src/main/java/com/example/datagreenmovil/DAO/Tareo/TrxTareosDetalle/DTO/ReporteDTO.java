package com.example.datagreenmovil.DAO.Tareo.TrxTareosDetalle.DTO;

public class ReporteDTO {
    public String NombreUsuario;
    public String Fecha;
    public String Horas;
    public String Consumidor;
    public String Actividad;
    public String Labor;
    public String Rendimientos;
    public int Personas;
    public String Jornales;
    public String Promedio;
    public int Items;

    public String getNombreUsuario() {
        return NombreUsuario;
    }

    public void setNombreUsuario(String nombreUsuario) {
        NombreUsuario = nombreUsuario;
    }

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String fecha) {
        Fecha = fecha;
    }

    public String getHoras() {
        return Horas;
    }

    public void setHoras(String horas) {
        Horas = horas;
    }

    public String getConsumidor() {
        return Consumidor;
    }

    public void setConsumidor(String consumidor) {
        Consumidor = consumidor;
    }

    public String getActividad() {
        return Actividad;
    }

    public void setActividad(String actividad) {
        Actividad = actividad;
    }

    public String getLabor() {
        return Labor;
    }

    public void setLabor(String labor) {
        Labor = labor;
    }

    public String getRendimientos() {
        return Rendimientos;
    }

    public void setRendimientos(String rendimientos) {
        Rendimientos = rendimientos;
    }

    public int getPersonas() {
        return Personas;
    }

    public void setPersonas(int personas) {
        Personas = personas;
    }

    public String getJornales() {
        return Jornales;
    }

    public void setJornales(String jornales) {
        Jornales = jornales;
    }

    public String getPromedio() {
        return Promedio;
    }

    public void setPromedio(String promedio) {
        Promedio = promedio;
    }

    public int getItems() {
        return Items;
    }

    public void setItems(int items) {
        Items = items;
    }
}